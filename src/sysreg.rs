impl SysReg {
    /// Get system register name
    ///
    /// # Examples
    ///
    /// ```
    /// use bad64::{decode, Operand, SysReg};
    /// // msr vbar_el3, x0
    /// let decoded = decode(0xd51ec000, 0).unwrap();
    ///
    /// let op = decoded.operand(0).unwrap();
    ///
    /// assert_eq!(op, Operand::SysReg(SysReg::VBAR_EL3));
    ///
    /// match op {
    ///     Operand::SysReg(sr) => assert_eq!(sr.name(), "vbar_el3"),
    ///     _ => assert!(false),
    /// };
    /// ```
    pub fn name(&self) -> &'static str {
        #[cfg(target_os = "windows")]
        {
            unsafe { CStr::from_ptr(bad64_sys::get_system_register_name(self.to_i32().unwrap())) }
                .to_str()
                .unwrap()
        }
        #[cfg(not(target_os = "windows"))]
        {
            unsafe { CStr::from_ptr(bad64_sys::get_system_register_name(self.to_u32().unwrap())) }
                .to_str()
                .unwrap()
        }
    }
}
